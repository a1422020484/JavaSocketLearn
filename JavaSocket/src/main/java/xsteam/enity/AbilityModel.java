package xsteam.enity;

public class AbilityModel {
	private int petAbilityId;
    private float hp;
    private float physicalDamage;
    private float physicalDefence;
    private float specialDamage;
    private float specialDefence;
    private float speed;
	public int getPetAbilityId() {
		return petAbilityId;
	}
	public void setPetAbilityId(int petAbilityId) {
		this.petAbilityId = petAbilityId;
	}
	public float getHp() {
		return hp;
	}
	public void setHp(float hp) {
		this.hp = hp;
	}
	public float getPhysicalDamage() {
		return physicalDamage;
	}
	public void setPhysicalDamage(float physicalDamage) {
		this.physicalDamage = physicalDamage;
	}
	public float getPhysicalDefence() {
		return physicalDefence;
	}
	public void setPhysicalDefence(float physicalDefence) {
		this.physicalDefence = physicalDefence;
	}
	public float getSpecialDamage() {
		return specialDamage;
	}
	public void setSpecialDamage(float specialDamage) {
		this.specialDamage = specialDamage;
	}
	public float getSpecialDefence() {
		return specialDefence;
	}
	public void setSpecialDefence(float specialDefence) {
		this.specialDefence = specialDefence;
	}
	public float getSpeed() {
		return speed;
	}
	public void setSpeed(float speed) {
		this.speed = speed;
	}

}
