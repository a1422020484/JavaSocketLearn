package nettyServer.templet.refdata;

public class TempletDataModel {

	private int mineId;
	private int mineType;
	private String mineRes;
	private int openLv;

	public int getMineId() {
		return mineId;
	}

	public void setMineId(int mineId) {
		this.mineId = mineId;
	}

	public int getMineType() {
		return mineType;
	}

	public void setMineType(int mineType) {
		this.mineType = mineType;
	}

	public String getMineRes() {
		return mineRes;
	}

	public void setMineRes(String mineRes) {
		this.mineRes = mineRes;
	}

	public int getOpenLv() {
		return openLv;
	}

	public void setOpenLv(int openLv) {
		this.openLv = openLv;
	}

}
